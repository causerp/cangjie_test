package com;
public class ReturnType {
    boolean aBoolean = true;
    byte aByte = 1;
    short aShort = 1;
    char aChar = 1;
    int aInt = 1;
    long aLong = 1;
    float aFloat = 1.0F;
    double aDouble = 1.0;

    public boolean getBoolean() {
        return aBoolean;
    }

    public byte getByte() {
        return aByte;
    }

    public short getShort() {
        return aShort;
    }

    public char getChar() {
        return aChar;
    }

    public int getInt() {
        return aInt;
    }

    public long getLong() {
        return aLong;
    }

    public float getFloat() {
        return aFloat;
    }

    public double getDouble() {
        return aDouble;
    }
}